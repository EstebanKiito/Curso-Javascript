// AND, OR, NOT

// AND (&&)
// true && true -> true
// true && false -> false

// OR (||)
// true || false -> true
// false || false -> false

// NOT (!)
// !true -> false

// Exemplo
const a = true;
const b = false;
const c = true;

console.log(a && b); // false
console.log(a || b); // true
console.log(!a); // false

