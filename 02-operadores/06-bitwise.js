// Bitwise OR
// 00000001
// 00000010
// 00000011 -> 3

// Bitwise AND
// 00000001
// 00000010
// 00000000 -> 0

// Bitwise XOR
// 00000001
// 00000010
// 00000011 -> 3

// Bitwise NOT
// 00000001
// 11111110 -> 254

console.log(1 | 2); // 3: 00000001 | 00000010 -> 00000011
console.log(1 & 2); // 0: 00000001 & 00000010 -> 00000000
console.log(1 ^ 2); // 3: 00000001 ^ 00000010 -> 00000011
console.log(~1); // -2: ~00000001 -> 11111110